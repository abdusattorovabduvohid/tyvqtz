export const DICT: Record<Lang, Dict> = { ru, uz };

// Выбор названия по языку с фолбэком на другой язык, если пусто.
export interface BilingualName {
  nameRu?: string | null;
  nameUz?: string | null;
}
export function pickName(
  o: BilingualName | null | undefined,
  lang: Lang
): string {
  if (!o) return "";
  if (lang === "uz") return (o.nameUz && o.nameUz.trim()) || o.nameRu || "";
  return (o.nameRu && o.nameRu.trim()) || (o.nameUz ?? "") || "";
}

export function translate(
  lang: Lang,
  key: string,
  params?: Record<string, string | number>
): string {
  let str = DICT[lang]?.[key] ?? DICT[DEFAULT_LANG][key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return str;
}
